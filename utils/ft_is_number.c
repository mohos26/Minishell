#include "../header.h"

int	ft_is_number(char *s)
{
	int	len;

	len = 0;
	if (*s == '+' || *s == '-')
		s++;
	if (!*s)
		return (0);
	while (len++ < 3 && ft_isdigit(*s))
		s++;
	return (!*s);
}
